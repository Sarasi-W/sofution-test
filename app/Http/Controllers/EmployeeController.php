    {
        $data = $request->all();

        $employee = Employee::create([
            'first_name' => $data['first_name'],
            'last_name' => $data['last_name'],
            'email' => $data['email'],
            'phone' => $data['phone'],
            'company_id' => $data['company'],
        ]);

        return redirect()->back()->with('success', 'The employee is successfully created.');
    }

